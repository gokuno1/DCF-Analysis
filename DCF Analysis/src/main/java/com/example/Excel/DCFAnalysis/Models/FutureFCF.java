package com.example.Excel.DCFAnalysis.Models;

public class FutureFCF {
	
	private int year;
	private double fcf;
	private double pvfcf;
	
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public double getFcf() {
		return fcf;
	}
	public void setFcf(double fcf) {
		this.fcf = fcf;
	}
	public double getPvfcf() {
		return pvfcf;
	}
	public void setPvfcf(double pvfcf) {
		this.pvfcf = pvfcf;
	}
	
	
}
